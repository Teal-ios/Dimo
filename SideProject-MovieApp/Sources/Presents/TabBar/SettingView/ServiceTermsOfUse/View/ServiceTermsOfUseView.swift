회원이 서비스를 이용할 경우에는 서비스를 이용하기까지 명시된 모든 내용에 동의한 것으로 간주합니다.

DIMO는 운영상·기술상의 필요에 따라 제공 중인 서비스를 변경할 수 있으며, 회원의 개인정보 및 서비스 이용 기록에 따라 서비스 이용에 차이를 둘 수 있습니다.

