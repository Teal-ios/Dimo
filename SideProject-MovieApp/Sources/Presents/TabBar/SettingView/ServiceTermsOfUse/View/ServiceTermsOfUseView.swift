- DIMO 및 기타 제3자의 저작권 등 지식재산권을 침해하는 행위
- DIMO 및 기타 제3자의 명예를 손상시키는 행위
- DIMO 서비스에서 얻은 정보를 DIMO의 사전 승낙 없이 영리적으로 이용하는 행위
- 불법적이거나 부당한 방법으로 DIMO의 업무를 방해하는 행위
- DIMO의 약관 또는 관련 법령을 준수하지 않는 행위
- 기타 사회질서에 위배되는 행위

이용자는 이 약관에서 DIMO가 규정한 사항을 준수해야 하며, 위의 의무를 소홀히 하여 손해가 발생하는 경우 그 책임은 이용자가 부담하고 DIMO는 해당 이용자에게 손해배상을 청구할 수 있습니다.

제6조 서비스의 중단

DIMO는 서비스의 제공을 전부 또는 일부를 일시적으로 제한하거나 중단할 수 있습니다. 이 경우 DIMO는 이용자가 지정한 전자우편 주소, 휴대전화로 이용자에게 통지합니다. 다만, DIMO가 사전에 통지할 수 없는 부득이한 사유가 있는 경우 사후에 통지할 수 있습니다.

- DIMO는 컴퓨터 등 정보통신 설비의 보수 점검, 교체, 고장, 통신 두절 등의 사유로 서비스 제공을 일시적으로 중단할 수 있습니다.
- DIMO는 천재지변, 전쟁, 경영 악화 등의 사유로 서비스를 더 이상 제공하기 어려울 경우, 통보 없이 서비스 제공을 영구적으로 중단할 수 있습니다.

제7조 회원가입

이용자는 DIMO에서 정한 가입 양식에 따라 회원정보를 기입한 후, 각 약관에 동의한다는 의사표시를 함으로써 회원가입을 신청합니다.

DIMO는 상기 이용자의 신청에 대하여 회원가입을 승낙함을 원칙으로 합니다. 다만, 다음 각 호에 해당하는 신청에 대하여는 승낙하지 않을 수 있습니다.

- 가입 신청자가 이전에 회원 자격을 상실한 적이 있는 경우
- 등록 내용에 허위, 기재누락, 오기가 있는 경우
- 회원 가입일 기준 만 14세 미만인 경우
- 이용자의 귀책사유로 승인이 불가능하거나 기타 규정 사항을 위반하여 신청하는 경우
- 악성 프로그램 이용이나 시스템 취약점을 악용하는 등 부정적인 방법을 서비스에 사용한 경우

DIMO는 각 호에 해당하는 경우 회원가입 신청에 대한 승낙을 제한할 수 있고, 그 사유가 해소될 때까지 승낙을 유보할 수 있습니다.

- 서비스 관련 설비의 여유가 없는 경우
- 기술상 혹은 업무상 문제가 있는 경우
- 기타 DIMO가 서비스 제공상 필요하다고 인정하는 경우

제8조 회원탈퇴 및 자격상실 등

회원은 언제든지 탈퇴를 요청할 수 있으며, DIMO는 회원의 요청에 따라 즉시 회원탈퇴를 처리합니다.

회원탈퇴 후 이용자 정보에 관한 기록은 개인정보 처리방침에 따라 관리 또는 폐기됩니다.

탈퇴 처리가 완료되었더라도, [이용자의 MBTI, 캐릭터 투표 데이터, 평가하기 및 찜하기 데이터]는 삭제되지 않습니다.

회원이 아래의 사유에 해당하는 경우, DIMO는 회원 자격을 제한 및 정지시킬 수 있습니다.

- 가입 신청 시에 허위 내용을 등록한 경우
- 다른 사람의 서비스 이용을 방해하거나 그 정보를 도용하는 등 이용 질서를 위협하는 경우
- 홍보·광고, 상업적 또는 악의적 목적으로 활동한 것이 확인된 경우
- 이벤트의 참여를 위해 여러 개의 계정을 생성하여 가입한 것이 확인된 경우
- 서비스 이용에 관한 법령 또는 이 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우

DIMO가 회원 자격을 제한·정지 시킨 후, 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 서비스에서 회원 자격을 상실시킬 수 있습니다.

회원 자격을 상실시키는 경우 DIMO는 회원 등록을 말소합니다. 이 경우 회원에게 이를 통지하고, 회원 등록 말소 전에 최소한 30일 이상의 기간을 정하여 소명할 기회를 부여합니다.

제9조 회원에 대한 통지 및 홍보 내용 표시

DIMO는 편리한 서비스 이용을 위해 서비스 이용과 관련된 각종 통지 및 서비스 홍보를 포함한 다양한 정보를 DIMO 화면 내에 표시하거나 이용자의 연락처 등으로 발송할 수 있으며, 이용자는 회원가입 전 해당 내용을 숙지하고 동의합니다.

DIMO가 회원에 대한 통지를 하는 경우 서비스 내부 알림 수단 또는 회원의 연락처를 이용할 수 있습니다.

DIMO는 회원 전체에 대한 통지를 하는 경우 서비스 내 공지사항 메뉴에 게시함으로써 개별 통지를 대체할 수 있습니다. 단, 회원의 권리 및 의무에 중대한 영향을 미치는 사항에 대해서는 개별 통지를 하는 것을 원칙으로 합니다.

DIMO가 제3조에서 회원에게 “거부 의사를 명시적으로 표현하지 않거나 약관 변경 이후에 서비스를 이용할 경우 변경된 약관에 동의한 것으로 간주한다”는 내용을 고지하였음에도 불구하고 회원이 의사를 표현하지 아니하면, DIMO는 통지 내용에 동의한 것으로 간주합니다.

제10조 개인정보의 관리 및 보호

DIMO가 이용자로부터 받은 개인정보는 서비스의 원활한 제공을 위해 이용자가 동의한 목적과 범위 내에서만 이용됩니다.

DIMO는 관련 법령이 정하는 바에 따라 회원의 개인정보를 보호하기 위해 노력합니다. 개인정보의 보호 및 이용에 관해서는 관련 법령 및 DIMO의 개인정보 처리방침을 따릅니다.

회원은 개인정보에 변동이 있을 경우, ‘내 정보 변경’ 메뉴를 이용하여 정보를 최신화해야 합니다.

회원의 아이디, 비밀번호, 이메일, 연락처 등 모든 개인정보의 관리 책임은 본인에게 있으므로, 타인에게 양도 및 대여할 수 없으며 유출되지 않도록 관리해야 합니다. 만약 본인의 아이디 및 비밀번호를 타인이 사용하고 있음을 인지했을 경우 즉시 문의 창구로 알려야 하고, 안내가 있는 경우 이에 따라야 합니다.

DIMO는 회원이 위의 내용을 이행하지 않아 발생한 피해에 대해, DIMO의 고의 또는 중대한 과실이 없는 한 어떠한 책임도 지지 않습니다.

제11조 저작권의 귀속

이용자가 서비스를 이용하는 과정에서 작성한 게시글, 댓글 등에 대한 저작권 및 기타 지식재산권은 별도의 의사표시가 없는 한 각 이용자에게 귀속됩니다.

이용자는 서비스를 이용함으로써 얻은 정보 중 서비스에게 지식재산권이 귀속된 정보를 DIMO의 사전 승낙 없이 출판, 복제, 방송, 전송 및 기타 방법에 의해 영리 목적으로 이용하거나 제3자가 이용하게 할 수 없습니다.

제12조 DIMO의 의무

DIMO는 이용자에게 서비스에 대해 상세히 설명해야 합니다.

