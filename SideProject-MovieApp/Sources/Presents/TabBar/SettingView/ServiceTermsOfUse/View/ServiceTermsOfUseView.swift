- 이용자의 귀책사유 또는 DIMO의 귀책사유가 아닌 사유로 발생한 이용자의 피해

제5조 서비스 이용 시 주의점

이용자는 다음 행위를 해서는 안 됩니다.

- 다른 이용자에게 비속어를 사용하는 행위
- 작품 또는 캐릭터의 스포일러를 통해 다른 이용자에게 불쾌감을 주는 행위
- 다른 이용자에게 폭언, 협박 또는 음란한 언행을 하는 등 사회질서에 위배되는 행위
- 타 서비스, 브랜드, 사이트, 애플리케이션, 사업체, 단체 등을 알리거나 가입·방문을 유도하기 위한 직·간접적 홍보 행위
- 서비스의 이용 과정에서 정치·종교적 발언을 하는 등 서비스의 직·간접적인 이용과 무관한 목적으로 서비스를 이용하는 행위
