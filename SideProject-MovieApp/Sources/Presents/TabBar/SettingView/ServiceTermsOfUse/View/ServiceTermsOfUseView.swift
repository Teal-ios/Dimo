DIMO는 다음 내용으로 발생한 피해에 대해서는 DIMO의 고의 또는 중대한 과실이 없는 한 어떠한 책임도 지지 않습니다.

- 모든 서비스, 게시물, 이용 기록의 진본성, 무결성, 신뢰성, 이용 가능성의 보장
- 서비스 이용 중 타인과 상호 간에 합의한 내용
- 게시물, 광고의 버튼, 하이퍼링크 등 외부로 연결된 서비스와 같이 DIMO가 제공하지 않은 서비스에서 발생한 피해
