제1조 목적

“DIMO 애플리케이션”(이하 “DIMO”) 서비스 이용약관은 DIMO가 DIMO 서비스를 제공함에 있어, DIMO와 이용자 간의 권리, 의무 및 책임사항 등을 규정하기 위해 작성되었습니다.

